//---------------------------------------------------------------------------
ForceGame::ForceGame(HINSTANCE hInstance)
:
Game(hInstance),
m_pShape(NULL)
{

}

//---------------------------------------------------------------------------
bool ForceGame::onInit()
{
	m_pShape = new Shape();

	m_pShape->setDim(100, 100);
	m_pShape->setPosXY(-200, 100);

	return true;
}

//---------------------------------------------------------------------------
bool ForceGame::onLoop()
{
	m_pShape->Draw(*g_graficos);

	return true;
}

//---------------------------------------------------------------------------
bool ForceGame::onDeInit()
{
	delete m_pShape;
	m_pShape = NULL;

	return true;
}

